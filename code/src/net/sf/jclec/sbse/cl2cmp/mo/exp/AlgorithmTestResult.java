package net.sf.jclec.sbse.cl2cmp.mo.exp;

public class AlgorithmTestResult {

	public String name;
	public double hv;
	public double spacing;
	public int hvType;
	public int sType;
	public double hvAlpha;
	public double spacingAlpha;
	
	public AlgorithmTestResult(){
		
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getHv() {
		return hv;
	}
	public void setHv(double hv) {
		this.hv = hv;
	}
	public double getSpacing() {
		return spacing;
	}
	public void setSpacing(double spacing) {
		this.spacing = spacing;
	}
	public int getHvType() {
		return hvType;
	}
	public void setHvType(int hvType) {
		this.hvType = hvType;
	}
	public int getsType() {
		return sType;
	}
	public void setsType(int sType) {
		this.sType = sType;
	}

	public double getHvAlpha() {
		return hvAlpha;
	}

	public void setHvAlpha(double hvAlpha) {
		this.hvAlpha = hvAlpha;
	}

	public double getSpacingAlpha() {
		return spacingAlpha;
	}

	public void setSpacingAlpha(double spacingAlpha) {
		this.spacingAlpha = spacingAlpha;
	}
	
	
}
